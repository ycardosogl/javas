public class Exemplo2 {
    public static void main(String[] args) {
        for(int i=0; i<=10; i++){
        System.out.println(i);
    }

    System.out.println("----------------------------------------------------------");
    
    //decre
    for(int x=20; x>=0; x--){
        System.out.println(x);
    }

    System.out.println("----------------------------------------------------------");
   
    //iniciando em 0 fim 100 em 5 em 5 
    for(int y=0; y<=100; y= y+5){
        System.out.println(y);
    }
   
    System.out.println("----------------------------------------------------------");
    //mostrar a soma dos valores 
    int soma = 0;
    for(int y=11; y<=30; y++){
        System.out.println(y);
        soma= soma + y;
    } System.out.println(soma);

    System.out.println("----------------------------------------------------------");
     
    


    }
}
